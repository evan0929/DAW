package com.popper.daw.bean;

public class DirectorOrderBean {
    private String name;
    private String time;

    public DirectorOrderBean(String name, String time) {
        this.name = name;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
